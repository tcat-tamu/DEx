package edu.tamu.tcat.dex.trc.entry.tei.transform;

public class ExtractManipulationException extends Exception
{

   public ExtractManipulationException()
   {
   }

   public ExtractManipulationException(String message)
   {
      super(message);
   }

   public ExtractManipulationException(Throwable cause)
   {
      super(cause);
   }

   public ExtractManipulationException(String message, Throwable cause)
   {
      super(message, cause);
   }

   public ExtractManipulationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
   {
      super(message, cause, enableSuppression, writableStackTrace);
   }

}
