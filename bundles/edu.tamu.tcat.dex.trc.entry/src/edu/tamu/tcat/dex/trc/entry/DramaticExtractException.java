package edu.tamu.tcat.dex.trc.entry;

public class DramaticExtractException extends Exception
{

   public DramaticExtractException()
   {
   }

   public DramaticExtractException(String message)
   {
      super(message);
   }

   public DramaticExtractException(Throwable cause)
   {
      super(cause);
   }

   public DramaticExtractException(String message, Throwable cause)
   {
      super(message, cause);
   }

   public DramaticExtractException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
   {
      super(message, cause, enableSuppression, writableStackTrace);
   }

}
